import Head from 'next/head';
import { useContext, useEffect, useState } from 'react';
import SiteContext from '../components/SiteContext';
import Navbar from '../components/Navbar';

export default function Register() {

  const { setcurrent, setSwipe, position } = useContext(SiteContext);

  useEffect(() => {
    setcurrent(5)
    setSwipe(false)
  }, [])

  // States for registration
  const [name, setName] = useState('');
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
 
  // States for checking the errors
  const [submitted, setSubmitted] = useState(false);
  const [error, setError] = useState(false);
 
  // Handling the name change
  const handleName = (e) => {
    setName(e.target.value);
    setSubmitted(false);
  };
 
  // Handling the email change
  const handleEmail = (e) => {
    setEmail(e.target.value);
    setSubmitted(false);
  };
 
  // Handling the password change
  const handlePassword = (e) => {
    setPassword(e.target.value);
    setSubmitted(false);
  };
 
  // Handling the form submission
  const handleSubmit = (e) => {
    e.preventDefault();
    if (name === '' || email === '' || password === '') {
      setError(true);
    } else {
      setSubmitted(true);
      setError(false);
    }
  };
 
  // Showing success message
  const successMessage = () => {
    return (
      <div
        className="success"
        style={{
          display: submitted ? '' : 'none',
        }}>
        <h1>User {name} successfully registered!!</h1>
      </div>
    );
  };
 
  // Showing error message if error is true
  const errorMessage = () => {
    return (
      <div
        className="error"
        style={{
          display: error ? '' : 'none',
        }}>
        <h1>Please enter all the fields</h1>
      </div>
    );
  };

  return (
    <div>
      <Head>
        <title>MINeD | Register</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      {/* Navigation Bar */}
      <Navbar />

      <main id="home">

        {/* Main Content here */}
        <div id="content" >
          <section style={{
            transform: `translateY(-${position*80}vh)`
          }}>
            <h1>User Registration</h1>  <br/>
        
            <form className='form'>
                {/* Labels and inputs for form data */}
                <label className="label">Name</label>
                <input onChange={handleName} className="input"
                value={name} type="text" /> <br/>
        
                <label className="label">Email</label>
                <input onChange={handleEmail} className="input"
                value={email} type="email" />  <br/>
        
                <label className="label">Password</label>
                <input onChange={handlePassword} className="input"
                value={password} type="password" /> <br/>
        
                <button onClick={handleSubmit} className="btn" type="submit">
                Submit
                </button>
            </form>

            {/* Calling to the methods */}
            <div className="messages">
                {errorMessage()}
                {successMessage()}
            </div>  <br/>

          </section>
        </div>

      </main>
    </div>
  )
}
